// set permissive hold per key you want it on... permissive hold activates key's secondary action if second key is pressed and released before key with hold modifier is released... this is annoying when using hyper on A key
bool get_permissive_hold(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case LSFT_T(KC_Z):
        case RSFT_T(KC_QUES):            // Immediately select the hold action when another key is tapped.
        case LCTL_T(KC_BSPC):
        case LGUI_T(KC_TAB):
        case LALT_T(KC_ENT):
        case LT(1,KC_SPC):
        case MO(2):
        case LT(6, KC_ESC):
            return true;
        default:
            // Do not select the hold action when another key is tapped.
            return false;
    }
}

